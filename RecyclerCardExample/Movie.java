package com.ben.recyclercardexample;

public class Movie {
    String name;
    int imgId;
    String rating;
    int year;
    String dec;

    public Movie(String name, int id, String rating, int year, String dec) {
        this.name = name;
        this.imgId = id;
        this.rating = rating;
        this.year = year;
        this.dec = dec;
    }

}
